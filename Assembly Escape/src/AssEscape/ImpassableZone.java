public class ImpassableZone {
	//literally just a box. making a class made coding it marginally easier so I did
	
	int x;
	int y;
	int width;
	int height;
	
	public ImpassableZone(int xx, int yy, int w, int h){
		x = xx;
		y = yy;
		width = w;
		height = h;
	}
}
